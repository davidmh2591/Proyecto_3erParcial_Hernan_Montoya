    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
       if (txtUsuario.getText().equals("")|| txtContrasenia.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Debe ingresar los datos solicitados");
        }else{
            boolean valor = conexion.InicioSesion(txtUsuario.getText(), txtContrasenia.getText());
            if (valor ==true){
                frmAnalogo ventanaA = new frmAnalogo();
                ventanaA.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Error con Usuario o Contraseña");
                txtUsuario.setText("");
                txtContrasenia.setText("");
                txtUsuario.requestFocus();
            }
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed

