    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        try {
            Usuario usuario = new Usuario();
           
            usuario.setNombre(txtNombre.getText());
            usuario.setApellido(txtApellido.getText());
            usuario.setEdad(Integer.parseInt(txtEdad1.getText().toString()));
            usuario.setUsuario(txtCrearUsuario.getText());
            usuario.setContrasenia(txtCrearContrasenia.getText());
            if (txtCrearContrasenia.getText().equals(txtConfirmarContrasenia.getText())){
                usuarioC.create(usuario);
                   JOptionPane.showMessageDialog(null, "Usuario Creado: "+usuario.getUsuario());
                    limpiar();
            }else{
                JOptionPane.showMessageDialog(null,"Contraseña y confirmacion de contraseña no coinciden");
                txtCrearContrasenia.setText("");
                txtConfirmarContrasenia.setText("");
                txtCrearContrasenia.requestFocus();
                
            }
           
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en crear" + e.getMessage());
        }
        
    }//GEN-LAST:event_btnCrearActionPerformed

